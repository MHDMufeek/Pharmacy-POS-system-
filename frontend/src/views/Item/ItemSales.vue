            />
          </div>
          <div v-if="amountPaid > 0" class="flex justify-between items-center text-sm">
            <span class="font-medium text-gray-700">Change:</span>
            <span class="font-bold text-green-600">Rs. {{ (amountPaid - totalAmount).toFixed(2) }}</span>
